#include<bits/stdc++.h>

using namespace std;
typedef long double ld;
const ld PI = 3.14;

int main()
{
    //ios_base::sync_with_stdio(false);
    //cin.tie(nullptr);
    ld x; cin >> x;
    cout << fixed << setprecision(4) <<  x * 2 << "\n";
    cout << fixed << setprecision(4) <<  x * 2 * PI << "\n";
    cout << fixed << setprecision(4) <<  x * x * PI << "\n";
    
    return 0;
}
